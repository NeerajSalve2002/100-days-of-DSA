public class arethmatic {
    public static void main(String[] args) {
        int a =10;
        int b =30;

        // addition
         int sum = a+b;

        //  substraction
        int sub = a-b;

        // multiplication
        int mul = a*b;

        // division
        int div = a/b;

        // modulo
        int modulo = a%b;

        // ++
        int plusplus = ++a;

        // --
        int minmin = --a;
        // outputs
        System.out.println("Sum: " + sum);
        System.out.println("Subtraction: " + sub);          
        System.out.println("Multiplication: " + mul);
        System.out.println("Division: " + div);
        System.out.println("Modulo: " + modulo);
        System.out.println("Increment: " + plusplus);
        System.out.println("Decrement: " + minmin);

    }
}
